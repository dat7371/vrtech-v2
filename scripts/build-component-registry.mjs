import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, "..");

const componentGroups = [
  {
    directory: "components/shared",
    keyForFile: (name) => name,
  },
  {
    directory: "components/landing",
    keyForFile: (name) => name,
  },
  {
    directory: "components/product",
    keyForFile: (name) => `product-${name}`,
  },
];

async function readComponentGroup({ directory, keyForFile }) {
  const absoluteDirectory = path.join(projectRoot, directory);
  const files = (await readdir(absoluteDirectory))
    .filter((file) => file.endsWith(".html"))
    .sort((left, right) => left.localeCompare(right));

  const entries = await Promise.all(
    files.map(async (file) => {
      const name = path.basename(file, ".html");
      const key = keyForFile(name);
      const source = await readFile(path.join(absoluteDirectory, file), "utf8");

      return [key, source.trim()];
    })
  );

  return entries;
}

const entries = (await Promise.all(componentGroups.map(readComponentGroup))).flat();
const registry = {};

for (const [key, source] of entries) {
  if (Object.hasOwn(registry, key)) {
    throw new Error(`Duplicate component key: ${key}`);
  }

  registry[key] = source;
}

const output = `// Generated by scripts/build-component-registry.mjs. Do not edit manually.\nwindow.COMPONENT_REGISTRY = ${JSON.stringify(registry, null, 2)};\n`;

await writeFile(path.join(projectRoot, "js/data/component-registry.js"), output, "utf8");
